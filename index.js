// Handle form submission
uploadFormElement.addEventListener("submit", function (e) {
    e.preventDefault();
    
    // Get the file input
    let fileInput = uploadFormElement.querySelector('input[type="file"]');
    let file = fileInput.files[0];

    // Validate file type and size (example: only allow images under 5MB)
    if (file) {
        const allowedTypes = ['image/jpeg', 'image/png', 'image/gif'];
        const maxSize = 5 * 1024 * 1024; // 5MB

        if (!allowedTypes.includes(file.type)) {
            alert("Please upload a valid image file (JPEG, PNG, GIF).");
            return;
        }

        if (file.size > maxSize) {
            alert("File size must be less than 5MB.");
            return;
        }

        // Handle the file upload logic here
        console.log("File uploaded:", file.name);
        
        // Reset the form
        uploadFormElement.reset();
        uploadForm.style.display = "none"; // Close the form after upload
    } else {
        alert("Please select a file to upload.");
    }
});















